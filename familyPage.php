				<div class="FPBText02">What is the weather like at Norm?</div>
				<div class="FPSText01">The weathjer in th austria area stays summer-like and warm- to the 70s and 80s through October. The region is cool and wet during the winter. Rainwear is essentail. A chilly morning fog that burns off by midday is typical. Clothing warm enough fot temperatures to the 30s is recommended from December to March. Spring temperatures return to the 70s, and many students quickly don shorts, T-shirts and sandals.</div>
