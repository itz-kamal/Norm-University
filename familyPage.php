	<div class="FPOth" id="FPTips">
		<div class="MCLSec floatL">
			<div class="FPBText01">Tips for Families</div>
			<div class="FFPMImgs"><img src="images/graduate02.jpeg" class="imageCover"></div>
			
			<div class="FPDesc">
				<div class="FPBText02">Let go.</div>
				<div class="FPSText01">Let them make their own decisions, solve their own problems, pick their own courses and, eventually, choose their own major. When they do choose a major, let the choice be based on intellectual passion, not preceptions of which major is safer or most pragmatic.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Listen and reassure.</div>
				<div class="FPSText01">At some point in their first year, many students feel overwhelmed. They may call home, fearing their admission was a mistake. We hear this offten. Listen, encourage and reassure. Suggest they talk to their academic advisor in their residence or thier resident fellow. Remind them of the support resources at Norm, such as tutoring, help with study skills and counseling.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Have conversations about lifestyle choices, especially alcohol use, before they come to campus.</div>
				<div class="FPSText01">Nationwide problems like binge drinking are challenges at Norm, too. Talk to your children in advance about the choices they are likely to comfront and help them determine what they value in their own behaviour and that of others.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Encourage them to know faculty members.</div>
				<div class="FPSText01">The academic program at Norm is esigned to encourage close interaction between students and faculty. From small freshman seminars to faculty nights in the dining halls, Norm offers opportunities for students to get to know faculty. Surveys shows that students who develop close relationships with faculty get more out of their Norm education then students whi do not.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Encourage them to learn for sake of learning.</div>
				<div class="FPSText01">Norm's general education requirement are designed to encourage intellectual exploration. Suggest they study a subject they have never studied before. Many students are acuustomed to collecting credentials needed for college admission. Encourage them, instead, to learn for the sake of learning.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Stay in touch, but perhaps not too much.</div>
				<div class="FPSText01">Between their busy schedules and changes in their sleeping patterns, it may be hard to reach them. Help foster their growing independence by, perhaps, being in touch less frequently than you are used to.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Don't focus on grades.</div>
				<div class="FPSText01">When you do talk to your students, try not to focuse on grades. Ask which faculty members they have met, which classes they enjoy, what are learning and what they are doing for fun. Norm students put plenty of pressure on themselves. What they need to know from you is that you believe in them.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Help them learn difference between disappointment and failure.</div>
				<div class="FPSText01">Many Norm students are used to being the smartest kids in their classes. At some point, they will experience something they will choose to label as failure-perhaps a "C" in a course-even if it is not. Not everyone can be first in his or her class. Assure them that their best effort is all you expect.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Send them a cake on their birthday.</div>
				<div class="FPSText01">The Parents' club has being to make Norm a home away-from-home for more than 37 years. Birthday cakes are delivered right to dorm rooms by members of the club.</div>
			</div>
			<div class="FPDesc">
				<div class="FPBText02">Call us if you are concerned or need reassurance</div>
				<div class="FPSText01">College and universities are severaly limited by fedral law about what information aboutstudents they can share with parents and guardians. Neverthsless, if you are concerned about a student or need some reassurance, contact the Paent Help Line at 01-395-45-035.</div>
